    protected void datatable() {
        Object[] Baris = {"id_film", "judul_film", "genre_film", "harga_tiket"};
        tabmode = new DefaultTableModel(null, Baris);
        tbldaftar.setModel(tabmode);
        String sql = "select * from film";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String id_film = hasil.getString("id_film");
                String judul_film = hasil.getString("judul_film");
                String genre_film = hasil.getString("genre_film");
                String harga_tiket = hasil.getString("harga_tiket");

                String[] data = {id_film, judul_film, genre_film, harga_tiket};
                tabmode.addRow(data);
            }
        } catch (Exception e) {

        }
    }

